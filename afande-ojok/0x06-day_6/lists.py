# Working with Lists Data Structure in Python

months = ["January", "February", "March", "April", "May", "June", "July", "August", "September", "October", "November", "December"]

print(months)
print(months[0])
print(months[8])
print(months[-1])
print(months[:6])
print(months[6:])
print(months[8:-1])

for i in months:
    print(i)
    print(i[0])
    print(i[:6])
    print(i[2:])
    print(i[0:3])